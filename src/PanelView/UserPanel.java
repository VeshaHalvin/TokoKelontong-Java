    private void simpanKasirButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanKasirButtonActionPerformed
        // TODO add your handling code here:
        int dialog = JOptionPane.showConfirmDialog(rootPane, "yakin ingin melakukan " + actionUser + "?");
        if(dialog == JOptionPane.CLOSED_OPTION || dialog == JOptionPane.NO_OPTION || dialog == JOptionPane.CANCEL_OPTION)
        return;
        try{
            inputKosongDataException();
            if(actionUser.equals("tambah")){
                dataModel = new User(usernameInputTextField.getText(), passwordInputTextField.getText(), 0);
                dataControl.insertUser(dataModel);
            }else{
                dataModel = new User(usernameInputTextField.getText(), passwordInputTextField.getText(), 0);
                dataControl.updateUser(dataModel, String.valueOf(idUser));
            }
            
        }catch(InputKosongException e){
            System.out.println(e);
        };
        clearTextData();
        setEditDeleteButtonData(false);
        setComponentsData(false);
        showUser();
        actionUser = null;
    }//GEN-LAST:event_simpanKasirButtonActionPerformed

