    private void hapusKasirButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusKasirButtonActionPerformed
        // TODO add your handling code here:
        clearTextData();
        setEditDeleteButtonData(false);
        setComponentsData(false);

        int opsi = JOptionPane.showConfirmDialog(rootPane, "Yakin Ingin Hapus ?", "Hapus Data", JOptionPane.YES_NO_OPTION);
        if( opsi == JOptionPane.NO_OPTION || opsi == JOptionPane.CLOSED_OPTION){
            return;
        }

        dataControl.deleteUser(String.valueOf(idUser));
        idUser = -1;
        showUser();
    }//GEN-LAST:event_hapusKasirButtonActionPerformed

