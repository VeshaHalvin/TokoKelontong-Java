    private void doSearchUser(){
        if(searchKasirTextField.getText().isEmpty())
            return;
            User c = dataControl.searchUser(searchKasirTextField.getText(),"");
            if(c == null ){
                JOptionPane.showMessageDialog(rootPane, "NOT FOUND !!!");
                return;
            }
            
            setEditDeleteButtonData(true);
            clearTextData();
            
            usernameInputTextField.setText(c.getUsername());
            passwordInputTextField.setText(c.getPassword());
            idUser=c.getId();
    }
