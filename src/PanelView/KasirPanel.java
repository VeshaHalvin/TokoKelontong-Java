    private void saveButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButton1ActionPerformed
        try {
            
            inputKosongPembelianException();
            idTransaksi= dataControl.generateId();
            int selectedIndexSingle = supplierDropdown.getSelectedIndex();
            Pelanggan selectedSingle = singleList.get(selectedIndexSingle);
            // itu masi temp
            TPenjualan dataNew = new TPenjualan(idTransaksi, selectedSingle, "2003-01-12", totalHarga);
            dataControl.insertTPenjualan(dataNew);
                
            for (int i = 0; i < pr.size(); i++) {
                BJual newB = new BJual(idTransaksi, pr.get(i), Jumlah.get(i));
                BLC.insertBJual(newB);
            }
            
        } catch (InputKosongException e) {
            JOptionPane.showMessageDialog(this, e.message());
        }catch(Exception e){
            System.out.println(e);
        }
        totalHarga=0;
        supplierDropdown.setSelectedIndex(-1);
        totalHargaTextArea.setText("");
        DefaultTableModel tblModel = (DefaultTableModel)kasirTable.getModel();
        tblModel.setRowCount(0);
        kasirTable.setModel(tblModel);
    }//GEN-LAST:event_saveButton1ActionPerformed

