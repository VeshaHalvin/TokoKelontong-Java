    public void InputKeTableTemp(){
        DefaultTableModel tblModel = (DefaultTableModel)kasirTable.getModel();
        Object rowData[] = new Object[5];
        for (int i = counter; i < Jumlah.size(); i++) {
            rowData[0] = counter + 1;
            rowData[1] = pr.get(i).getNama();
            rowData[2] = pr.get(i).getHarga_satuan();
            rowData[3] = Jumlah.get(i);
            rowData[4] = pr.get(i).getHarga_satuan() * Jumlah.get(i);
            tblModel.addRow(rowData);
            totalHarga += pr.get(i).getHarga_satuan() * Jumlah.get(i);
        }
        
    }
    public void SearchProdukNyaNgab(String kode,int Jumlah){
        P=PrC.searchProduk(kode);
        if((P.getJumlah()-Jumlah)<0){
            JOptionPane.showMessageDialog(rootPane, "Habis Stok Ngab");
            return;
        }
        InputKeArrayList(Jumlah, P);
    }
    public void InputKeArrayList(int jumlah, Produk E){
        Jumlah.add(jumlah);
        pr.add(E);
        InputKeTableTemp();
        totalHargaTextArea.setText(""+totalHarga);
        counter+=1;
    }
    
