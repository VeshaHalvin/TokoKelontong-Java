    private void barangTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_barangTableMouseClicked
        actionProduk = "baharui";

        tambahDataButton.setEnabled(false);
        cancelButton.setEnabled(true);
        saveButton.setEnabled(true);
        setEditDeleteBtn(true);

        setComponent(false);
        setRadioComponent(false);
        buttonGroup1.clearSelection();

        int clickedRow = barangTable.getSelectedRow();
        TableModel tableModel = barangTable.getModel();

        idProduk = tableModel.getValueAt(clickedRow, 0).toString();
        kodeProdukInput.setText(idProduk);

        namaProdukInput.setText(tableModel.getValueAt(clickedRow, 1).toString());
        hargaSatuanInput.setText(tableModel.getValueAt(clickedRow, 4).toString());
        jumlahInput.setText(tableModel.getValueAt(clickedRow, 3).toString());
        String radio = tableModel.getValueAt(clickedRow, 2).toString();

        switch(radio){
            case "Alat Rumah Tangga" :
            radioData1.setSelected(true);
            break;
            case "Makanan dan Minuman" :
            radioData2.setSelected(true);
            break;
            case "Kecantikan":
            radioData3.setSelected(true);
            break;
        }

        cancelButton.setEnabled(true);
    }//GEN-LAST:event_barangTableMouseClicked

