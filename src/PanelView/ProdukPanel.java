    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        int dialog = JOptionPane.showConfirmDialog(rootPane, "yakin ingin melakukan " + actionProduk + "?");
        if(dialog == JOptionPane.CLOSED_OPTION || dialog == JOptionPane.NO_OPTION || dialog == JOptionPane.CANCEL_OPTION)
        return;
        try{
            // EXCEPTION
            inputKosongPembelianException();

            // SET ATRIBUT DAN METODE PEMBAYARAN
            String radio = "";

            radio = buttonGroup1.getSelection().getActionCommand();

            if(actionProduk.equals("tambah")){
                Produk dataNew = new Produk(idProduk, namaProdukInput.getText(), radio, Integer.parseInt(jumlahInput.getText()), Double.parseDouble(hargaSatuanInput.getText()));
                dataControl.insertProduk(dataNew);
            } else {
                Produk dataNew = new Produk( namaProdukInput.getText(), radio, Integer.parseInt(jumlahInput.getText()), Double.parseDouble(hargaSatuanInput.getText()));
                dataControl.updateProduk(dataNew,idProduk);
                idProduk = "";
            }
        }catch(InputKosongException e){
            JOptionPane.showMessageDialog(this, e.message());
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "ANGKA NGAB");
        }

        clearText();
        setComponent(false);
        setEditDeleteBtn(false);
        setRadioComponent(false);
        buttonGroup1.clearSelection();
        tambahDataButton.setEnabled(true);
        showTableBySearch("");
        actionProduk=null;
    }//GEN-LAST:event_saveButtonActionPerformed

