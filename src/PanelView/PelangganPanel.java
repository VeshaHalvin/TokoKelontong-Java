    private void simpanPelangganButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanPelangganButtonActionPerformed
        // TODO add your handling code here:
        String radio;
        boolean radio2;
        radio = GrupSex.getSelection().getActionCommand();
        radio2 = Boolean.parseBoolean(GrupMember.getSelection().getActionCommand());
        int dialog = JOptionPane.showConfirmDialog(rootPane, "yakin ingin melakukan " + actionPelanggan + "?");
        if(dialog == JOptionPane.CLOSED_OPTION || dialog == JOptionPane.NO_OPTION || dialog == JOptionPane.CANCEL_OPTION)
        return;
        try{
            inputKosongDataException();
            if(actionPelanggan.equals("tambah")){
                dataModel = new Pelanggan(radio, radio2, idPelanggan, namaPelangganInputTextField.getText(), AlamatInputTextField1.getText(), nomorTeleponInputTextField2.getText());
                dataControl.insertPelanggan(dataModel);
            } else {
                dataModel = new Pelanggan(radio, radio2, namaPelangganInputTextField.getText(), AlamatInputTextField1.getText(), nomorTeleponInputTextField2.getText());
                dataControl.updatePelanggan(dataModel, idPelanggan);
            }
            
        }catch(InputKosongException e){
            System.out.println(e);
        }
        clearTextData();
        setEditDeleteButtonData(false);
        setComponentsData(false);
        showTableBySearch("");
        actionPelanggan = null;
        tambahPelangganButton.setEnabled(true);
    }//GEN-LAST:event_simpanPelangganButtonActionPerformed

