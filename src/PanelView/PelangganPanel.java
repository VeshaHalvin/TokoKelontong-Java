    private void pelangganTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_pelangganTableMouseClicked
        int indexSingle = -1, indexMulti = -1;
        actionPelanggan = "baharui";

        tambahPelangganButton.setEnabled(false);
        simpanPelangganButton.setEnabled(true);
        setEditDeleteButtonData(true);

        setComponentsData(false);
        setRadioComponent(false);
        GrupMember.clearSelection();
        GrupSex.clearSelection();
        int clickedRow = pelangganTable.getSelectedRow();
        TableModel tableModel = pelangganTable.getModel();

        idPelanggan = tableModel.getValueAt(clickedRow, 0).toString();

        idPelangganInputTextField.setText(idPelanggan);
        AlamatInputTextField1.setText(tableModel.getValueAt(clickedRow, 3).toString());
        namaPelangganInputTextField.setText(tableModel.getValueAt(clickedRow, 1).toString());
        nomorTeleponInputTextField2.setText(tableModel.getValueAt(clickedRow, 1).toString());
        
        String radio1 = tableModel.getValueAt(clickedRow, 2).toString();
        String radio2 = tableModel.getValueAt(clickedRow, 5).toString();
        switch(radio2){
            case "true" :
            radioData4.setSelected(true);
            break;
            case "false" :
            radioData5.setSelected(true);
            break;
        }
        
        switch(radio1){
            case "Laki-Laki" :
            radioData6.setSelected(true);
            break;
            case "Perempuan" :
            radioData7.setSelected(true);
            break;
        }
    }//GEN-LAST:event_pelangganTableMouseClicked


